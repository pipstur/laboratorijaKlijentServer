    private void setUp(boolean flag) {
        txtOpisRez.setLineWrap(true); 
        txtOpisRez.setWrapStyleWord(true);
        cbVrstaRezultata.setModel(new DefaultComboBoxModel<>(VrstaRezultata.values()));
        cbVrstaRezultata.setSelectedIndex(-1);
        String[] argumenti = {"",""};
        try {
            Controller.getInstance().pretragaEksperimenata(argumenti);
            
        } catch (Exception ex) {
            Logger.getLogger(FrmRezultat.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(rezultat.getId()!=0){
            txtNazivRez.setText(rezultat.getNaziv());
            txtOpisRez.setText(rezultat.getOpis());
            cbVrstaRezultata.setSelectedItem(rezultat.getVrstaRezultata());
            txtNazivRez.setEditable(flag);
            txtOpisRez.setEditable(flag);
            cbVrstaRezultata.setEnabled(flag);
            btnSacuvaj.setVisible(flag);
        }
    }

    private void ubaciUBazu(Rezultat rezultat) {
        
        if(rezultat.getId()==0){
            try {
            Controller.getInstance().kreirajRezultat(rezultat);
            
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        
        }else{
            try {
                Controller.getInstance().izmeniRezultat(rezultat);
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        
    }
    public void kreirajRezultat(boolean flag) throws Exception{
        if(flag){
                JOptionPane.showMessageDialog(this, "Uspešno kreiran rezultat! ");
                Controller.getInstance().setFrmRezultat(null);
                dispose();
            }else{
              JOptionPane.showMessageDialog(this, "Greška pri kreiranju rezultata");
              Controller.getInstance().setFrmRezultat(null);
              dispose();
            }
    }
    public void izmeniRezultat(boolean flag) throws Exception{
        if(flag){
            JOptionPane.showMessageDialog(this, "Uspešna izmena rezultata!");
            Controller.getInstance().setFrmRezultat(null);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Neuspešna izmena rezultata!");
            Controller.getInstance().setFrmRezultat(null);
            dispose();
        }
    }
    public void namestiCb(List<Eksperiment> eksperimenti){
        cbEksperimenti.setModel(new DefaultComboBoxModel<>( eksperimenti.toArray()));
        Eksperiment eksperimentSelektovani = new Eksperiment();
        for(Eksperiment eksp : eksperimenti){
            if(eksperiment.getId() == eksp.getId()){
                eksperimentSelektovani = eksp;
            }
        }
        cbEksperimenti.setSelectedItem(eksperimentSelektovani);
        lblEksperiment.setText(eksperiment.toString());
    }
