    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        rezultat.setEksperiment((Eksperiment)cbEksperimenti.getSelectedItem());
        rezultat.setNaziv(txtNazivRez.getText());
        rezultat.setOpis(txtOpisRez.getText());
        rezultat.setVrstaRezultata((VrstaRezultata) cbVrstaRezultata.getSelectedItem());
        
        if(rezultat.getNaziv().isEmpty()||rezultat.getNaziv().length()>100){
            JOptionPane.showMessageDialog(this, "Naziv je neispravan, duzi od 100 karaktera ili prazan!");
        }
        else if(rezultat.getOpis().isEmpty()||rezultat.getOpis().length()>255){
            JOptionPane.showMessageDialog(this, "Opis je neispravan, duzi od 255 karaktera ili prazan!");
        }
        else if(rezultat.getVrstaRezultata()==null){
            JOptionPane.showMessageDialog(this, "Selektujte vrstu rezultata!");
        }
        else{
            ubaciUBazu(rezultat);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

