    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        eksperiment.setNaziv(txtNazivEksperimenta.getText());
        eksperiment.setOpis(txtOpisEksperimenta.getText());
        eksperiment.setKorisceneSupstance(txtKorisceneSupstance.getText());
        eksperiment.setVrstaEksperimenta( (VrstaEksperimenta) cbVrstaEksp.getSelectedItem());
        
        if(eksperiment.getNaziv().isEmpty()||eksperiment.getNaziv().length()>100){
            JOptionPane.showMessageDialog(this, "Naziv je neispravan");
        }
        else if(eksperiment.getOpis().isEmpty()||eksperiment.getOpis().length()>255){
            JOptionPane.showMessageDialog(this, "Opis je neispravan");
        }
        else if(eksperiment.getKorisceneSupstance().isEmpty()||eksperiment.getKorisceneSupstance().length()>255){
            JOptionPane.showMessageDialog(this, "Koriscene supsatnce su neispravne");
        }
        else if(eksperiment.getVrstaEksperimenta()==null){
            JOptionPane.showMessageDialog(this, "Selektujte vrstu eksperimenta");
        }
        else{
            ubaciUBazu(eksperiment);
        }
        
    }//GEN-LAST:event_btnKreirajActionPerformed

