    private void populateFields(Eksperiment eksperiment, boolean flag) {
        txtOpisEksperimenta.setLineWrap(true);
        txtOpisEksperimenta.setWrapStyleWord(true);
        cbVrstaEksp.setModel(new DefaultComboBoxModel(VrstaEksperimenta.values()));
        
        txtOpisEksperimenta.setEditable(flag);
        txtNazivEksperimenta.setEditable(flag);
        txtKorisceneSupstance.setEditable(flag);
        cbVrstaEksp.setEnabled(flag);
        btnKreiraj.setVisible(flag);
        
        
        if(eksperiment.getId()!=0){
            lblNaslov.setText("Izmena eksperimenta");
            if(!flag){
            lblNaslov.setText("Detalji eksperimenta");
            }
            txtNazivEksperimenta.setText(eksperiment.getNaziv());
            txtKorisceneSupstance.setText(eksperiment.getKorisceneSupstance());
            txtOpisEksperimenta.setText(eksperiment.getOpis());
            cbVrstaEksp.setSelectedIndex(eksperiment.getVrstaEksperimenta().getId()-1);
            btnKreiraj.setText("Izmeni eksperiment");
        }
        else{
            JOptionPane.showMessageDialog(this, "Sistem je kreirao novi eksperiment");
            cbVrstaEksp.setSelectedIndex(-1);
        }
        
    }

    private void ubaciUBazu(Eksperiment eksperiment) {
        if(eksperiment.getId()==0){
            try {
                controller.kreirajEksperiment(eksperiment);
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        
        }else{
            try {
                controller.izmeniEksperiment(eksperiment);
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        
        
    }
    public void kreiranEksperiment(boolean flag) throws Exception{
        if(flag){
            JOptionPane.showMessageDialog(this, "Uspešno dodavanje eksperimenta!");
            Controller.getInstance().setFrmEksperiment(null);
            dispose();
            
        }else{
            JOptionPane.showMessageDialog(this, "Neuspešno dodavanje eksperimenta!");
            Controller.getInstance().setFrmEksperiment(null);
            dispose();
        }
    }
    public void izmenjenEksperiment(boolean flag) throws Exception{
                if(flag){
                    JOptionPane.showMessageDialog(this, "Uspešna izmena eksperimenta!");
                    Controller.getInstance().setFrmEksperiment(null);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Neuspešna izmena eksperimenta!");
                    Controller.getInstance().setFrmEksperiment(null);
                    dispose();
                }
    }
