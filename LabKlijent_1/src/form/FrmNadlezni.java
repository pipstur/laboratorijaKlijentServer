    private void btnIzmenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmenaActionPerformed
        int rowIndex = tblRezultati.getSelectedRow();
        RezultatiTableModel model = (RezultatiTableModel) tblRezultati.getModel();
        Rezultat rezultat = model.getRezultatAt(rowIndex);
        if(rezultat!=null){
            JFrame frmIzmena = new FrmRezultat(rezultat.getEksperiment(),rezultat, true);
            frmIzmena.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Niste selektovali nijedan rezultat! ");
        }
    }//GEN-LAST:event_btnIzmenaActionPerformed

    private void btnPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretragaActionPerformed
        String kolona = "";
        if(cbPretraga.getSelectedItem().equals("Naziv Eksperimenta")) kolona = "e.naziv";
        else if(cbPretraga.getSelectedItem().equals("Naziv Rezultata")) kolona = "r.naziv";
        else if(cbPretraga.getSelectedItem().equals("Vrsta Rezultata")) kolona = "Vrsta Rezultata";
        else kolona ="r.opis";
        String kriterijum = txtPretraga.getText();
        String[] argumenti = {kriterijum, kolona};
        
        try {
            Controller.getInstance().pretragaRezultata(argumenti);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnPretragaActionPerformed

